#include "minishell.h"

// READ
// EVALUATE
// PRINT
// LOOP
int	main(void)
{
	char	*prompt;
	//int		argc;
	//char	**argv;

	ft_print_banner();
	while (1)
	{
		// read input from user
		prompt = readline(">>> ");
		if (prompt == NULL)
			printf(RED "ERROR: readline\n" RESET);
		// add input to history
		if (prompt[0] != '\0')
			add_history(prompt);
		if (!ft_check_quotes(prompt))
		{
			// parse and tokenize input
			// execute command
			printf("You typed %s\n", prompt);
		}
		// free memory
		free (prompt);
	}
	return (0);

}

